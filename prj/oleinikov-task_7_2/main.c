#include <stdio.h>
#include <locale.h>
#include <windows.h>

// Функція для виведення інформації про програму та розробника
void print_about() {
    printf("=== Анотація ===\n");
    printf("Ця програма змінює місцями першу половину масиву з другою та обчислює суму трьох останніх елементів.\n");
    printf("Розробник: Олейніков Олександр Сергійович КБ24\n");
    printf("=================\n\n");
}

int main() {
    // Налаштування локалі та кодування консолі для Windows
    SetConsoleOutputCP(65001); // Встановлення вихідного кодування консолі на UTF-8
    setlocale(LC_ALL, "uk_UA.UTF-8"); // Встановлення локалі для української мови

    // Виведення інформації про програму та розробника
    print_about();

    // Оголошення масиву з 14 елементів
    int arr[14];
    int i, temp;

    // Введення елементів масиву
    printf("Введіть 14 елементів масиву:\n");
    for (i = 0; i < 14; i++) {
        printf("Елемент %d: ", i + 1);
        scanf("%d", &arr[i]);
    }

    // Зміна місцями першої половини масиву з другою
    for (i = 0; i < 7; i++) {
        temp = arr[i];
        arr[i] = arr[i + 7];
        arr[i + 7] = temp;
    }

    // Виведення модифікованого масиву
    printf("\nМодифікований масив:\n");
    for (i = 0; i < 14; i++) {
        printf("%d ", arr[i]);
    }
    printf("\n");

    // Обчислення суми трьох останніх елементів
    int sum = arr[11] + arr[12] + arr[13];
    printf("\nСума трьох останніх елементів: %d\n", sum);

    return 0;
}
